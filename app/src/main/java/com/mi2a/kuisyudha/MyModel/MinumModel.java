package com.mi2a.kuisyudha.MyModel;

public class MinumModel {

    private int imageMinum;
    private String namaMinum;
    private String hargaMinum;

    public int getImageMinum() {
        return imageMinum;
    }

    public void setImageMinum(int imageMinum) {
        this.imageMinum = imageMinum;
    }

    public String getNamaMinum() {
        return namaMinum;
    }

    public void setNamaMinum(String namaMinum) {
        this.namaMinum = namaMinum;
    }

    public String getHargaMinum() {
        return hargaMinum;
    }

    public void setHargaMinum(String hargaMinum) {
        this.hargaMinum = hargaMinum;
    }
}
