package com.kn0en.simplemateriallogin.MyModel;

public class MakanModel {
    private String namaMakan;
    private String hargaMakan;
    private int imageMakan;

    public String getNamaMakan() {
        return namaMakan;
    }

    public void setNamaMakan(String namaMakan) {
        this.namaMakan = namaMakan;
    }

    public String getHargaMakan() {
        return hargaMakan;
    }

    public void setHargaMakan(String hargaMakan) {
        this.hargaMakan = hargaMakan;
    }

    public int getImageMakan() {
        return imageMakan;
    }

    public void setImageMakan(int imageMakan) {
        this.imageMakan = imageMakan;
    }
}
